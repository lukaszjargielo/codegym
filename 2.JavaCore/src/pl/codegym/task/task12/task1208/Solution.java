public class Solution {
    public static void main(String[] args) {

    }

    //tutaj wpisz swój kod

    public static void print(String s) {

        System.out.println("s: " + s);

    }

    public static void print(String str1, String str2) {

        System.out.println("str1: " + str1 + "/nstr2: " + str2);
    }

    public static void print(String str1, int i) {

        System.out.println("str1: " + str1 + "/ni: " + i);
    }

    public static void print(int i, String str1) {

        System.out.println("/ni: " + i + "str1: " + str1);

    }

    public static void print(int i) {

        System.out.println("i: " + i);

    }

}
